// =============================================================================
// rv32ui_p_all.S — RV32I 全指令自检测试
// =============================================================================
// 参考 riscv-tests rv32ui-p-* 风格，覆盖 RV32I 所有指令。
// 通过：gp=1，失败：gp=失败编号
// =============================================================================

#include "rvp_test_macros.h"

.text
.globl _start
_start:
    INIT_TESTS

    # ========================================================================
    # 1. ADDI (test 2-6)
    # ========================================================================
    li   x1, 0
    addi x1, x1, 5            # x1 = 5
    TEST_CASE(2, x1, 5)

    addi x2, x0, -1           # x2 = -1 = 0xFFFFFFFF
    TEST_CASE(3, x2, -1)

    addi x3, x0, 2047         # 最大 12 位正立即数
    TEST_CASE(4, x3, 2047)

    addi x4, x0, -2048        # 最小 12 位负立即数
    TEST_CASE(5, x4, -2048)

    # ========================================================================
    # 2. ADD / SUB (test 7-10)
    # ========================================================================
    li   x5, 100
    li   x6, 27
    add  x7, x5, x6           # 127
    TEST_CASE(7, x7, 127)

    sub  x8, x5, x6           # 73
    TEST_CASE(8, x8, 73)

    sub  x9, x6, x5           # -73
    li   x10, -73
    TEST_CASE(9, x9, -73)     # 注意：用寄存器比较因为立即数限制

    add  x10, x2, x1          # -1 + 5 = 4
    TEST_CASE(10, x10, 4)

    # ========================================================================
    # 3. AND / OR / XOR (test 11-14)
    # ========================================================================
    li   x11, 0xFF
    li   x12, 0x0F
    and  x13, x11, x12        # 0x0F = 15
    TEST_CASE(11, x13, 15)

    or   x14, x11, x12        # 0xFF = 255
    TEST_CASE(12, x14, 255)

    xor  x15, x11, x12        # 0xF0 = 240
    TEST_CASE(13, x15, 240)

    xori x16, x11, 0xFF       # 0
    TEST_CASE(14, x16, 0)

    # ========================================================================
    # 4. SLL / SRL / SRA (test 15-18)
    # ========================================================================
    li   x17, 1
    slli x18, x17, 4          # 16
    TEST_CASE(15, x18, 16)

    li   x19, 0x80000000
    srli x20, x19, 28         # 0x8 = 8 (逻辑右移)
    TEST_CASE(16, x20, 8)

    srai x21, x19, 28         # 0xFFFFFFF8 = -8 (算术右移)
    li   x22, -8
    TEST_CASE(17, x21, -8)

    sll  x23, x17, x6         # 1 << 27
    li   x24, 0x08000000
    TEST_CASE(18, x23, 0x08000000)

    # ========================================================================
    # 5. SLT / SLTU (test 19-22)
    # ========================================================================
    li   x25, -5
    li   x26, 3
    slt  x27, x25, x26        # -5 < 3 = 1
    TEST_CASE(19, x27, 1)

    slt  x28, x26, x25        # 3 < -5 = 0
    TEST_CASE(20, x28, 0)

    sltu x29, x25, x26        # 0xFFFFFFFB < 3 = 0 (无符号)
    TEST_CASE(21, x29, 0)

    sltu x30, x26, x25        # 3 < 0xFFFFFFFB = 1
    TEST_CASE(22, x30, 1)

    # ========================================================================
    # 6. LUI / AUIPC (test 23-24)
    # ========================================================================
    lui x1, 0x12345
    li  x2, 0x12345000
    TEST_CASE(23, x1, 0x12345000)

    auipc x3, 0
    # x3 = PC of this instruction，无法预知精确值，跳过严格检查
    # 改测：auipc + addi 组合
    auipc x4, 0x1            # x4 = PC + 0x1000
    sub   x5, x4, x3         # x5 = 0x1000
    li    x6, 0x1000
    TEST_CASE(24, x5, 0x1000)

    # ========================================================================
    # 7. JAL / JALR (test 25-26)
    # ========================================================================
    jal  x1, jal_target
    li   x2, 99              # 不该执行
jal_target:
    li   x2, 42
    TEST_CASE(25, x2, 42)

    # JALR
    la   x3, jalr_target     # 注意：la 可能编译成多条指令
    jalr x4, x3, 0
    li   x5, 88             # 不该执行
jalr_target:
    li   x5, 77
    TEST_CASE(26, x5, 77)

    # ========================================================================
    # 8. Branch: BEQ / BNE (test 27-30)
    # ========================================================================
    li   x6, 7
    li   x7, 7
    beq  x6, x7, beq_ok
    li   x8, 99             # 不该执行
beq_ok:
    li   x8, 55
    TEST_CASE(27, x8, 55)

    li   x9, 1
    li   x10, 2
    bne  x9, x10, bne_ok
    li   x11, 99            # 不该执行
bne_ok:
    li   x11, 66
    TEST_CASE(28, x11, 66)

    # ========================================================================
    # 9. Branch: BLT / BGE (test 29-32)
    # ========================================================================
    li   x12, -3
    li   x13, 5
    blt  x12, x13, blt_ok    # -3 < 5 → 跳
    li   x14, 99
blt_ok:
    li   x14, 44
    TEST_CASE(29, x14, 44)

    bge  x13, x12, bge_ok    # 5 >= -3 → 跳
    li   x15, 99
bge_ok:
    li   x15, 33
    TEST_CASE(30, x15, 33)

    bltu x12, x13, bltu_fail  # 0xFFFFFFFD < 5? 否(无符号)
    j    bltu_ok
bltu_fail:
    li   x16, 99
bltu_ok:
    li   x16, 22
    TEST_CASE(31, x16, 22)

    bgeu x12, x13, bgeu_ok    # 0xFFFFFFFD >= 5? 是(无符号)
    li   x17, 99
bgeu_ok:
    li   x17, 11
    TEST_CASE(32, x17, 11)

    # ========================================================================
    # 10. Load / Store (test 33-38)
    # ========================================================================
    # 字存取
    li   x1, 0xDEADBEEF
    sw   x1, 0(x0)           # MEM[0] = DEADBEEF
    lw   x2, 0(x0)
    li   x3, 0xDEADBEEF
    TEST_CASE(33, x2, 0xDEADBEEF)

    # 半字存取
    li   x4, 0x1234
    sh   x4, 4(x0)           # MEM[4] = 0x1234
    lh   x5, 4(x0)
    TEST_CASE(34, x5, 0x1234)

    lhu  x6, 4(x0)
    TEST_CASE(35, x6, 0x1234)

    # 字节存取
    li   x7, 0xAB
    sb   x7, 8(x0)           # MEM[8] = 0xAB
    lb   x8, 8(x0)
    li   x9, 0xFFFFFFAB      # 符号扩展
    TEST_CASE(36, x8, 0xFFFFFFAB)

    lbu  x10, 8(x0)
    TEST_CASE(37, x10, 0xAB)

    # 负字节
    li   x11, 0x80
    sb   x11, 12(x0)
    lb   x12, 12(x0)         # 0xFFFFFF80
    TEST_CASE(38, x12, 0xFFFFFF80)

    # ========================================================================
    # 11. M 扩展：MUL / MULH / MULHU (test 39-41)
    # ========================================================================
    li   x1, 7
    li   x2, 6
    mul  x3, x1, x2          # 42
    TEST_CASE(39, x3, 42)

    li   x4, -5
    li   x5, 3
    mul  x6, x4, x5          # -15
    TEST_CASE(40, x6, -15)

    # MULHU: 0xFFFFFFFF * 0xFFFFFFFF = 0xFFFFFFFE00000001, 高 32 位 = 0xFFFFFFFE
    li   x7, -1
    mulhu x8, x7, x7
    li   x9, 0xFFFFFFFE
    TEST_CASE(41, x8, 0xFFFFFFFE)

    # ========================================================================
    # 12. M 扩展：DIV / DIVU / REM / REMU (test 42-45)
    # ========================================================================
    li   x10, 100
    li   x11, 7
    div  x12, x10, x11       # 14
    TEST_CASE(42, x12, 14)

    divu x13, x10, x11       # 14
    TEST_CASE(43, x13, 14)

    rem  x14, x10, x11       # 2
    TEST_CASE(44, x14, 2)

    remu x15, x10, x11       # 2
    TEST_CASE(45, x15, 2)

    # ========================================================================
    # 全部通过
    # ========================================================================
    TEST_PASS

fail:
    j fail

pass:
    j pass

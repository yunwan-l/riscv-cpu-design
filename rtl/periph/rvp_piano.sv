// =============================================================================
// rvp_piano.sv - RVP 钢琴外设（硬件音符查表器）
// =============================================================================
// 功能：CPU 写入音符索引，硬件查表返回对应频率。
//       硬件实现音符-频率映射表，体现硬件设计创新。
//
// 寄存器映射（基址 0x1004_0000）：
//   0x00: NOTE  (R/W) - 音符索引 (0=静音, 1=C4, 2=D4, ..., 16=D6)
//   0x04: FREQ  (R)   - 频率 (Hz)，硬件查表返回，0 表示静音
//
// 音符表（16 音，C4 ~ D6，C 大调）：
//   1=C4(262)  2=D4(294)  3=E4(330)  4=F4(349)
//   5=G4(392)  6=A4(440)  7=B4(494)  8=C5(523)
//   9=D5(587) 10=E5(659) 11=F5(698) 12=G5(784)
//  13=A5(880) 14=B5(988) 15=C6(1047) 16=D6(1175)
//
// 总线协议：与 rvp_gpio / rvp_timer 一致（简单同步写 + 异步读）
// =============================================================================

module rvp_piano (
  input  logic        clk_i,
  input  logic        rst_ni,

  // 总线接口（与 GPIO/Timer/UART 一致）
  input  logic [3:0]  addr_i,
  input  logic        read_i,
  input  logic        write_i,
  input  logic [31:0] wdata_i,
  output logic [31:0] rdata_o
);

  // -------------------------------------------------------------------------
  // 音符索引寄存器（CPU 可读写）
  // -------------------------------------------------------------------------
  logic [4:0] note_reg;  // 0-16
  logic [26:0] unused_wdata;  // 消除 wdata_i[31:5] 未使用警告

  assign unused_wdata = wdata_i[31:5];

  always_ff @(posedge clk_i or negedge rst_ni) begin
    if (!rst_ni)
      note_reg <= 5'd0;
    else if (write_i && addr_i == 4'h0)
      note_reg <= wdata_i[4:0];  // 取低 5 位（0-16）
  end

  // -------------------------------------------------------------------------
  // 硬件音符-频率查表（综合后映射为 LUT/ROM）
  // -------------------------------------------------------------------------
  logic [31:0] freq;

  always_comb begin
    unique case (note_reg)
      5'd1:  freq = 32'd262;   // C4
      5'd2:  freq = 32'd294;   // D4
      5'd3:  freq = 32'd330;   // E4
      5'd4:  freq = 32'd349;   // F4
      5'd5:  freq = 32'd392;   // G4
      5'd6:  freq = 32'd440;   // A4
      5'd7:  freq = 32'd494;   // B4
      5'd8:  freq = 32'd523;   // C5
      5'd9:  freq = 32'd587;   // D5
      5'd10: freq = 32'd659;   // E5
      5'd11: freq = 32'd698;   // F5
      5'd12: freq = 32'd784;   // G5
      5'd13: freq = 32'd880;   // A5
      5'd14: freq = 32'd988;   // B5
      5'd15: freq = 32'd1047;  // C6
      5'd16: freq = 32'd1175;  // D6
      default: freq = 32'd0;   // 静音
    endcase
  end

  // -------------------------------------------------------------------------
  // 读数据多路选择
  // -------------------------------------------------------------------------
  always_comb begin
    rdata_o = 32'b0;
    if (read_i) begin
      unique case (addr_i)
        4'h0:    rdata_o = {27'b0, note_reg};  // NOTE 寄存器
        4'h4:    rdata_o = freq;                // FREQ（硬件查表）
        default: rdata_o = 32'b0;
      endcase
    end
  end

endmodule : rvp_piano

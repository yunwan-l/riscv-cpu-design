// =============================================================================
// rvp_register_file.sv — RVP 寄存器堆
// =============================================================================
// 功能：32 个 32 位寄存器（x0~x31），2 个读端口 + 1 个写端口。
//
// 接口：
//   clk_i     : 时钟
//   rst_ni    : 异步复位（低有效）。复位时全部清零（仿真可预测，FPGA 可选）
//   we_i      : 写使能（1=本周期写 rd）
//   waddr_i   : 写地址（rd，5 位）
//   wdata_i   : 写数据（32 位）
//   raddr1_i  : 读端口1 地址（rs1）
//   rdata1_o  : 读端口1 数据 → 通常送 ALU 的 operand_a
//   raddr2_i  : 读端口2 地址（rs2）
//   rdata2_o  : 读端口2 数据 → 通常送 ALU 的 operand_b
//
// 设计要点：
//   1. x0 永远为 0：读 x0 返回 0，写 x0 被忽略（RISC-V 硬性规定）
//   2. 异步读 + 同步写：读是组合逻辑（给地址立即出数据），
//      写在时钟上升沿生效。单周期 CPU 一个周期内就能读出数据给 ALU。
//   3. 写优先级：同一周期对同一地址"又写又读"，读到的是旧值
//      （写在时钟沿才生效，当前周期读的是沿之前的值）。流水线阶段再处理。
//   4. FPGA 实现：32×32=1Kbit，综合器会推断为分布式 RAM 或寄存器，
//      不占用 BRAM（BRAM 有时钟要求，不适合异步读）。
// =============================================================================

module rvp_register_file (
  input  logic        clk_i,
  input  logic        rst_ni,

  // 写端口
  input  logic        we_i,
  input  logic [4:0]  waddr_i,
  input  logic [31:0] wdata_i,

  // 读端口 1 (rs1)
  input  logic [4:0]  raddr1_i,
  output logic [31:0] rdata1_o,

  // 读端口 2 (rs2)
  input  logic [4:0]  raddr2_i,
  output logic [31:0] rdata2_o
);

  // -------------------------------------------------------------------------
  // 寄存器阵列：32 个 32 位寄存器
  // -------------------------------------------------------------------------
  // 注意：这里声明 32 个元素（索引 0~31），x0 虽然物理存在但永不写入，
  // 读出时被强制为 0，等效于"不存在"。
  logic [31:0] regs [31:0];

  // -------------------------------------------------------------------------
  // 同步写 + 异步复位
  // -------------------------------------------------------------------------
  // rst_ni=0 时全部清零，确保仿真时寄存器不是 X（不可预测）。
  // FPGA 上若不想用复位（省资源），可删掉 else 分支，上电靠程序初始化。
  always_ff @(posedge clk_i or negedge rst_ni) begin
    if (!rst_ni) begin
      for (int i = 0; i < 32; i++) begin
        regs[i] <= 32'b0;
      end
    end else if (we_i && (waddr_i != 5'd0)) begin
      // x0 不可写：waddr_i==0 时本条件为假，写被忽略
      regs[waddr_i] <= wdata_i;
    end
  end

  // -------------------------------------------------------------------------
  // -------------------------------------------------------------------------
  // 异步读
  // -------------------------------------------------------------------------
  // 读 x0 返回 0，其余直接读数组。
  // 注意：不做"写优先读"——同周期写读同地址读到旧值。
  // 流水线的 WB→ID 前递在 core 里单独处理（避免组合环路）。
  assign rdata1_o = (raddr1_i == 5'd0) ? 32'b0 : regs[raddr1_i];
  assign rdata2_o = (raddr2_i == 5'd0) ? 32'b0 : regs[raddr2_i];

endmodule : rvp_register_file

module rvp_core_pipeline (
  input  logic clk_i,
  input  logic rst_ni,

  // ????????????
  output logic [31:0] pc_o,
  output logic [31:0] instr_o,
  output logic        illegal_o,

  // ?????????????????????SoC ??????
  // CPU ?????????????????????????????????????????????????????? RAM / UART / GPIO / Timer
  output logic [31:0]             dbus_addr_o,      // ??????????????????????????????
  output logic                    dbus_read_o,       // ?????????
  output logic                    dbus_write_o,      // ?????????
  output rvp_pkg::mem_size_e      dbus_size_o,       // ???????????? B/H/W
  output logic                    dbus_unsigned_o,   // ????????????
  output logic [31:0]             dbus_wdata_o,      // ?????????
  input  logic [31:0]             dbus_rdata_i,      // ???????????????

  // ????????????????????????SoC ??????????????????
  output logic [31:0]             perf_cycle_o,      // ????????????
  output logic [31:0]             perf_inst_o,       // ???????????????
  output logic [31:0]             perf_stall_o,      // Load-Use ?????????
  output logic [31:0]             perf_flush_o,      // ???????????????
  output logic [31:0]             perf_branch_o,     // ??????/???????????????

  // I-Cache 统计计数器
  output logic [31:0]             icache_hit_o,      // I-Cache 命中次数
  output logic [31:0]             icache_miss_o      // I-Cache 未命中次数
);

  import rvp_pkg::*;

  // =========================================================================
  // ?????????????????????
  // =========================================================================
  // IF ???
  logic [31:0] if_pc, if_pc_next, if_instr;

  // IF/ID ??????
  logic [31:0] id_pc, id_instr;

  // ID ???
  ctrl_t       id_ctrl;
  logic [31:0] id_imm;
  logic [31:0] id_rs1_data, id_rs2_data;
  logic [31:0] id_rs1_fwd, id_rs2_fwd;  // WB???ID ???????????????

  // ID/EX ??????
  logic [31:0] ex_pc;
  ctrl_t       ex_ctrl;
  logic [31:0] ex_rs1_data, ex_rs2_data, ex_imm;

  // EX ???
  logic [1:0]  forward_a, forward_b;
  logic [31:0] ex_alu_op_a, ex_alu_op_b;
  logic [31:0] ex_alu_result;
  logic        ex_alu_cmp;  // 1-bit: ALU ??????????????????????????????????????????
  logic [31:0] ex_forward_a_val, ex_forward_b_val;
  logic [31:0] ex_multdiv_result;    // M ?????????????????????
  logic [31:0] ex_result;            // ALU ??? multdiv ?????????????????????

  // Multdiv 控制信号
  logic multdiv_start, multdiv_done, multdiv_idle;
  logic multdiv_stall;

  // EX/MEM ??????
  logic [31:0] mem_pc;
  ctrl_t       mem_ctrl;
  logic [31:0] mem_alu_result, mem_rs2_data, mem_imm;

  // MEM ???
  logic [31:0] mem_rdata;
  logic [31:0] mem_imm_wb;   // lui ??? imm ?????? WB ???
  logic [31:0] mem_wb_data;  // MEM ??????????????????????????? EX/MEM ?????????

  // MEM/WB ??????
  logic [31:0] wb_pc;
  ctrl_t       wb_ctrl;
  logic [31:0] wb_alu_result, wb_rdata;
  logic [31:0] wb_imm;        // lui ??? imm

  // WB ???
  logic [31:0] wb_data;

  // =========================================================================
  // ????????????
  // =========================================================================
  logic stall;            // Load-Use ??????
  logic branch_taken;     // ????????????
  logic flush_if_id;      // ?????? IF/ID
  logic flush_id_ex;      // ?????? ID/EX
  logic take_jump;        // ???????????????jal/jalr???????????????

  // =========================================================================
  // 1. IF ????????????
  // =========================================================================
  // PC ?????????
  always_ff @(posedge clk_i or negedge rst_ni) begin
    if (!rst_ni)
      if_pc <= 32'h0;
    else if (stall | multdiv_stall)
      if_pc <= if_pc;        // ?????????PC ??????
    else
      if_pc <= if_pc_next;   // ????????????
  end

  // I-Cache：PMRU 8路组相联指令缓存 + 16入口流式预取 + 流式旁路
  // 后备存储为 rvp_instr_mem（双端口 BRAM）
  rvp_icache_pmru8 icache (
    .clk_i         (clk_i),
    .rst_ni        (rst_ni),
    .addr_i        (if_pc),
    .instr_o       (if_instr),
    .hit_o         (),           // 单周期命中信号（统计用，暂不外连）
    .miss_o        (),
    .hit_count_o   (icache_hit_o),
    .miss_count_o  (icache_miss_o)
  );

  // =========================================================================
  // 2. IF/ID ?????????
  // =========================================================================
  rvp_pipeline_regs pipe_regs (
    .clk_i(clk_i), .rst_ni(rst_ni),

    // IF/ID
    .if_id_flush_i (flush_if_id),
    .if_id_stall_i (stall | multdiv_stall),
    .if_pc_i       (if_pc),
    .if_instr_i    (if_instr),
    .id_pc_o       (id_pc),
    .id_instr_o    (id_instr),

    // ID/EX
    .id_ex_flush_i (flush_id_ex),
    .id_ex_stall_i (multdiv_stall),
    .id_pc_i       (id_pc),
    .id_ctrl_i     (id_ctrl),
    .id_rs1_data_i (id_rs1_fwd),
    .id_rs2_data_i (id_rs2_fwd),
    .id_imm_i      (id_imm),
    .ex_pc_o       (ex_pc),
    .ex_ctrl_o     (ex_ctrl),
    .ex_rs1_data_o (ex_rs1_data),
    .ex_rs2_data_o (ex_rs2_data),
    .ex_imm_o      (ex_imm),

    // EX/MEM
    .ex_mem_flush_i (1'b0),
    .ex_mem_stall_i (multdiv_stall),
    .ex_pc_i        (ex_pc),
    .ex_ctrl_i      (ex_ctrl),
    .ex_alu_result_i(ex_result),       // ALU ??? multdiv ??????
    .ex_rs2_data_i  (ex_forward_b_val),  // store ??????????????????
    .ex_imm_i       (ex_imm),
    .mem_pc_o       (mem_pc),
    .mem_ctrl_o     (mem_ctrl),
    .mem_alu_result_o(mem_alu_result),
    .mem_rs2_data_o (mem_rs2_data),
    .mem_imm_o      (mem_imm),

    // MEM/WB
    .mem_wb_flush_i (1'b0),
    .mem_wb_stall_i (multdiv_stall),
    .mem_pc_i       (mem_pc),
    .mem_ctrl_i     (mem_ctrl),
    .mem_alu_result_i(mem_alu_result),
    .mem_rdata_i    (mem_rdata),
    .wb_pc_o        (wb_pc),
    .wb_ctrl_o      (wb_ctrl),
    .wb_alu_result_o(wb_alu_result),
    .wb_rdata_o     (wb_rdata)
  );

  // =========================================================================
  // 3. ID ???????????? + ???????????? + ???????????????
  // =========================================================================
  rvp_decoder decoder (
    .instr_i (id_instr),
    .ctrl_o  (id_ctrl)
  );

  rvp_imm_generator imm_gen (
    .instr_i    (id_instr),
    .imm_type_i (id_ctrl.imm_type),
    .imm_o      (id_imm)
  );

  rvp_register_file reg_file (
    .clk_i    (clk_i),
    .rst_ni   (rst_ni),
    .we_i     (wb_ctrl.reg_write),
    .waddr_i  (wb_ctrl.rd_addr),
    .wdata_i  (wb_data),
    .raddr1_i (id_ctrl.rs1_addr),
    .rdata1_o (id_rs1_data),
    .raddr2_i (id_ctrl.rs2_addr),
    .rdata2_o (id_rs2_data)
  );

  // -------------------------------------------------------------------------
  // WB???ID ??????????????????????????? write-first???
  // -------------------------------------------------------------------------
  // ?????????????????????????????? + ??????????????? WB ?????? rd ????????? ID ???????????????????????????
  //       ID ????????????????????????????????????????????????????????????????????????????????????
  // ??????????????? WB ???????????? rd?????? rd == ID ???????????? rs1/rs2???????????? wb_data???
  //       ?????? EX ???????????????????????????????????????????????? ID ??????
  // ?????????rd==x0 ???????????????x0 ????????? 0??????
  assign id_rs1_fwd = (wb_ctrl.reg_write && (wb_ctrl.rd_addr != 5'd0) &&
                       (wb_ctrl.rd_addr == id_ctrl.rs1_addr)) ? wb_data : id_rs1_data;
  assign id_rs2_fwd = (wb_ctrl.reg_write && (wb_ctrl.rd_addr != 5'd0) &&
                       (wb_ctrl.rd_addr == id_ctrl.rs2_addr)) ? wb_data : id_rs2_data;

  // =========================================================================
  // 4. EX ??????ALU ?????? + ??????
  // =========================================================================
  // ????????????
  rvp_forward_unit forward_unit (
    .ex_rs1_addr_i  (ex_ctrl.rs1_addr),
    .ex_rs2_addr_i  (ex_ctrl.rs2_addr),
    .mem_rd_addr_i  (mem_ctrl.rd_addr),
    .mem_reg_write_i(mem_ctrl.reg_write),
    .wb_rd_addr_i   (wb_ctrl.rd_addr),
    .wb_reg_write_i (wb_ctrl.reg_write),
    .forward_a_o    (forward_a),
    .forward_b_o    (forward_b)
  );

  // ??????????????????
  always_comb begin
    unique case (forward_a)
      2'b00:   ex_forward_a_val = ex_rs1_data;
      2'b01:   ex_forward_a_val = wb_data;          // MEM/WB ??????
      2'b10:   ex_forward_a_val = mem_wb_data;      // EX/MEM ??????????????????????????????
      default: ex_forward_a_val = ex_rs1_data;
    endcase

    unique case (forward_b)
      2'b00:   ex_forward_b_val = ex_rs2_data;
      2'b01:   ex_forward_b_val = wb_data;
      2'b10:   ex_forward_b_val = mem_wb_data;      // EX/MEM ??????????????????????????????
      default: ex_forward_b_val = ex_rs2_data;
    endcase
  end

  // ALU ???????????????
  assign ex_alu_op_a = ex_ctrl.alu_op_a_sel ? ex_pc : ex_forward_a_val;
  assign ex_alu_op_b = ex_ctrl.alu_op_b_sel ? ex_imm : ex_forward_b_val;

  // ALU
  rvp_alu alu (
    .alu_op_i     (ex_ctrl.alu_op),
    .operand_a_i  (ex_alu_op_a),
    .operand_b_i  (ex_alu_op_b),
    .result_o     (ex_alu_result),
    .cmp_result_o (ex_alu_cmp)
  );

  // ??????????????????M ???????????? ???????????????????????????

  // Multdiv 控制信号
  // start: 当 use_multdiv=1 且 multdiv 空闲时启动
  assign multdiv_start = ex_ctrl.use_multdiv && multdiv_idle;
  // stall: 当 use_multdiv=1 且结果未就绪时停顿全流水线
  assign multdiv_stall = ex_ctrl.use_multdiv && !multdiv_done;
  rvp_multdiv multdiv (
    .clk_i       (clk_i),
    .rst_ni      (rst_ni),
    .flush_i     ((branch_taken || take_jump) && !multdiv_stall),
    .start_i     (multdiv_start),
    .op_i        (ex_ctrl.multdiv_op),
    .operand_a_i (ex_forward_a_val),
    .operand_b_i (ex_forward_b_val),
    .result_o    (ex_multdiv_result),
    .done_o      (multdiv_done),
    .idle_o      (multdiv_idle)
  );

  // ???????????????M ??????????????? multdiv ?????????????????? ALU ??????
  assign ex_result = ex_ctrl.use_multdiv ? ex_multdiv_result : ex_alu_result;

  // =========================================================================
  // 5. MEM ?????????????????????????????? data_mem ?????? SoC ?????????
  // =========================================================================
  // CPU ?????????????????? data_mem??????????????????????????????????????????
  // SoC ????????????????????????????????????????????????????????? RAM / UART / GPIO / Timer???
  assign dbus_addr_o      = mem_alu_result;
  assign dbus_wdata_o     = mem_rs2_data;
  assign dbus_read_o      = mem_ctrl.mem_read;
  assign dbus_write_o     = mem_ctrl.mem_write;
  assign dbus_size_o      = mem_ctrl.mem_size;
  assign dbus_unsigned_o  = mem_ctrl.mem_unsigned;
  assign mem_rdata        = dbus_rdata_i;

  // -------------------------------------------------------------------------
  // ?????? MEM ???????????????????????????????????? EX/MEM ?????????
  // ?????? bug???????????????????????? mem_alu_result?????? lui???wb_sel=WB_IMM???????????? 0
  //          ?????? lui ??? ALU ?????? 0+0=0???alu_op_b_sel ????????? rs2=x0???
  //          ???????????? wb_sel ?????????????????????????????? WB ???????????????
  // -------------------------------------------------------------------------
  always_comb begin
    unique case (mem_ctrl.wb_sel)
      WB_ALU: mem_wb_data = mem_alu_result;
      WB_MEM: mem_wb_data = mem_rdata;
      WB_PC4: mem_wb_data = mem_pc + 32'd4;
      WB_IMM: mem_wb_data = mem_imm;
      default: mem_wb_data = mem_alu_result;
    endcase
  end

  // =========================================================================
  // 6. WB ??????????????????
  // =========================================================================
  // lui ??? imm ????????? EX ?????? WB??????????????????????????????
  always_ff @(posedge clk_i or negedge rst_ni) begin
    if (!rst_ni) begin
      mem_imm_wb <= 32'b0;
      wb_imm     <= 32'b0;
    end else if (!multdiv_stall) begin
      // multdiv_stall 期间冻结，与 pipe_regs 的 EX/MEM、MEM/WB 保持一致
      mem_imm_wb <= ex_imm;       // EX → MEM
      wb_imm     <= mem_imm_wb;   // MEM → WB
    end
  end

  always_comb begin
    unique case (wb_ctrl.wb_sel)
      WB_ALU: wb_data = wb_alu_result;
      WB_MEM: wb_data = wb_rdata;
      WB_PC4: wb_data = wb_pc + 32'd4;
      WB_IMM: wb_data = wb_imm;   // lui
      default: wb_data = 32'b0;
    endcase
  end

  // =========================================================================
  // 7. ???????????????Load-Use ??????
  // =========================================================================
  rvp_hazard_unit hazard_unit (
    .id_ex_mem_read_i(ex_ctrl.mem_read),
    .id_ex_rd_addr_i (ex_ctrl.rd_addr),
    .if_id_rs1_addr_i(id_ctrl.rs1_addr),
    .if_id_rs2_addr_i(id_ctrl.rs2_addr),
    .stall_o         (stall)
  );

  // =========================================================================
  // 8. ?????????????????? EX ??????
  // =========================================================================
  rvp_branch_unit branch_unit (
    .is_branch_i   (ex_ctrl.branch),
    .cmp_result_i  (ex_alu_cmp),
    .branch_taken_o(branch_taken)
  );

  // ??????????????????????????????jal/jalr ??? EX ?????????????????????
  // ex_ctrl.next_pc != PC_SEQ ?????????????????????????????????
  // ???????????????branch_taken=1????????????next_pc=PC_JUMP ??? PC_JALR
  assign take_jump = (ex_ctrl.next_pc == PC_JUMP) || (ex_ctrl.next_pc == PC_JALR);

  // =========================================================================
  // 9. ????????????
  // =========================================================================
  // ????????????????????????????????????IF/ID ??? ID/EX ????????????????????????????????????
  assign flush_if_id = (branch_taken || take_jump) && !(stall | multdiv_stall);
  // 分支命中时需要 flush ID/EX（杀死错误路径指令），Load-Use stall 时也 flush
  // multdiv_stall 期间不 flush（流水线冻结，且 multdiv 不是分支指令）
  assign flush_id_ex = stall | ((branch_taken || take_jump) && !(stall | multdiv_stall));

  // =========================================================================
  // 10. ????????? PC ??????
  // =========================================================================
  always_comb begin
    if (stall | multdiv_stall) begin
      if_pc_next = if_pc;   // ?????????PC ??????
    end else begin
      unique case (ex_ctrl.next_pc)
        PC_SEQ:    if_pc_next = if_pc + 32'd4;
        PC_BRANCH: if_pc_next = branch_taken ? (ex_pc + ex_imm) : (if_pc + 32'd4);
        PC_JUMP:   if_pc_next = ex_pc + ex_imm;
        PC_JALR:   if_pc_next = (ex_forward_a_val + ex_imm) & ~32'b1;
        default:   if_pc_next = if_pc + 32'd4;
      endcase
    end
  end

  // =========================================================================
  // 11. ???????????????
  // =========================================================================
  // 5 ??? 32 ?????????????????????????????????????????????????????????
  // ?????? SoC ?????? MMIO???0x1003_0000??????????????? lw ?????????
  //
  // ??????????????????
  //   cycle_count   : ?????????????????? +1??????????????? 0 ?????????
  //   inst_retired  : WB ??????????????????????????? +1???????????? bubble/NOP ?????????
  //   stall_count   : Load-Use ??????????????????stall ????????? +1???
  //   flush_count   : ??????/????????????????????????flush_if_id ????????? +1???
  //   branch_count  : EX ?????????????????????/???????????????
  //
  // inst_retired ?????????????????? valid ?????????????????????????????????????????? bubble???
  //   - ??????????????????????????? NOP ????????? invalid????????????
  //   - flush ????????? bubble ????????? invalid
  //   - stall ????????? bubble???ID/EX???????????? invalid
  // =========================================================================

  // --- ???????????????????????? ---
  logic [31:0] perf_cycle_r, perf_inst_r, perf_stall_r, perf_flush_r, perf_branch_r;

  // --- valid ????????????????????? pipeline_regs????????????????????????---
  logic id_valid_q, ex_valid_q, mem_valid_q, wb_valid_q;

  // --- ??????/?????????????????????EX ??????---
  logic ex_is_branch;
  assign ex_is_branch = ex_ctrl.branch ||
                        (ex_ctrl.next_pc == PC_JUMP) ||
                        (ex_ctrl.next_pc == PC_JALR);

  // --- valid ??????????????? ---
  // id_valid_q: IF/ID ??????????????????????????????????????? bubble???
  //   flush_if_id=1 ??? invalid????????????????????? bubble???
  //   stall=1       ??? ?????????????????? ID ????????????
  //   ??????           ??? valid=1??????????????? IF ?????? ID???
  //
  // ex_valid_q: ID/EX ?????????????????????????????????
  //   flush_id_ex=1 ??? invalid???stall ????????? bubble???
  //   ??????           ??? ?????? id_valid_q
  //
  // mem_valid_q / wb_valid_q: ????????????
  always_ff @(posedge clk_i or negedge rst_ni) begin
    if (!rst_ni) begin
      id_valid_q  <= 1'b0;
      ex_valid_q  <= 1'b0;
      mem_valid_q <= 1'b0;
      wb_valid_q  <= 1'b0;
    end else if (multdiv_stall) begin
      // Multdiv stall: freeze all valid signals
      id_valid_q  <= id_valid_q;
      ex_valid_q  <= ex_valid_q;
      mem_valid_q <= mem_valid_q;
      wb_valid_q  <= wb_valid_q;
    end else begin
      // IF ??? ID
      if (flush_if_id)
        id_valid_q <= 1'b0;
      else if (!stall)
        id_valid_q <= 1'b1;

      // ID ??? EX
      if (flush_id_ex)
        ex_valid_q <= 1'b0;
      else
        ex_valid_q <= id_valid_q;

      // EX ??? MEM
      mem_valid_q <= ex_valid_q;

      // MEM ??? WB
      wb_valid_q <= mem_valid_q;
    end
  end

  // --- ????????????????????? ---
  always_ff @(posedge clk_i or negedge rst_ni) begin
    if (!rst_ni) begin
      perf_cycle_r   <= 32'b0;
      perf_inst_r    <= 32'b0;
      perf_stall_r   <= 32'b0;
      perf_flush_r   <= 32'b0;
      perf_branch_r  <= 32'b0;
    end else begin
      // cycle_count: ????????? +1
      perf_cycle_r <= perf_cycle_r + 32'd1;

      // inst_retired: WB ?????????????????? +1
      if (wb_valid_q)
        perf_inst_r <= perf_inst_r + 32'd1;

      // stall_count: Load-Use ? multdiv ????????????
      if (stall | multdiv_stall)
        perf_stall_r <= perf_stall_r + 32'd1;

      // flush_count: ??????/??????????????????
      if (flush_if_id)
        perf_flush_r <= perf_flush_r + 32'd1;

      // branch_count: EX ??????????????????/????????????
      if (ex_is_branch && ex_valid_q)
        perf_branch_r <= perf_branch_r + 32'd1;
    end
  end

  // --- ????????????????????? ---
  assign perf_cycle_o  = perf_cycle_r;
  assign perf_inst_o   = perf_inst_r;
  assign perf_stall_o  = perf_stall_r;
  assign perf_flush_o  = perf_flush_r;
  assign perf_branch_o = perf_branch_r;

  // =========================================================================
  // ????????????
  // =========================================================================
  assign pc_o      = if_pc;
  assign instr_o   = if_instr;
  assign illegal_o = id_ctrl.illegal;

endmodule : rvp_core_pipeline

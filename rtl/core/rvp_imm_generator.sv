 *     格式: imm[12] = instr[31], imm[10:5] = instr[30:25],
 *           imm[4:1] = instr[11:8], imm[11] = instr[7], imm[0] = 0, 符号扩展
 *   IMM_U - U型: 用于LUI, AUIPC
 *     格式: imm[31:12] = instr[31:12], imm[11:0] = 0
 *   IMM_J - J型: 用于JAL
 *     格式: imm[20] = instr[31], imm[10:1] = instr[30:21],
 *           imm[11] = instr[20], imm[19:12] = instr[19:12], imm[0] = 0, 符号扩展
 *   IMM_Z - Z型: 用于CSR指令
 *     格式: imm[4:0] = instr[19:15], 零扩展
 */

